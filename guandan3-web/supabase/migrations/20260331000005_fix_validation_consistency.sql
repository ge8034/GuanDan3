# 前后端验证逻辑一致性修复报告

## 执行时间
2026-03-31

## 问题概述
前端 rules.ts 与后端 validate_guandan_move SQL 函数存在多处严重不一致，导致：
1. 无效牌型可能通过后端验证
2. 级牌炸弹大小计算错误
3. 王炸没有特殊优先级处理

## 详细不一致分析

### 1. 炸弹检测（CRITICAL）

**前端行为：**
- 王炸：4张王，primaryValue = 10000
- 级牌炸弹：4张以上级牌，primaryValue = 5000 × 张数 + 最大牌值
- 普通炸弹：4张以上相同，primaryValue = 1000 × 张数 + 最大牌值

**后端行为：**
- 只检测4张以上相同
- 没有区分王炸、级牌炸弹、普通炸弹
- 直接使用 val 比较，不考虑级牌特殊值（50/60）

### 2. 牌型验证（HIGH）

**前端支持的牌型：**
- single（单张）
- pair（对子）
- triple（三张）
- straight（顺子，5张连续，不含2和王）
- sequencePairs（连对，3对以上连续）
- sequenceTriples（连三，2个以上连续三张）
- fullhouse（三带二）
- bomb（炸弹，4种类型）
- pass（过牌）

**后端行为：**
- 只检查炸弹和牌数
- 不检查具体牌型有效性
- 允许任意相同牌数的组合通过

### 3. 顺子规则（MEDIUM）

**前端：**
- 必须连续
- 不能包含2（val=15）和王
- 至少5张

**后端：**
- 没有顺子规则检查

### 4. 连对规则（MEDIUM）

**前端：**
- 至少3对
- 必须连续
- 总张数必须是偶数

**后端：**
- 没有连对规则检查

### 5. 三带二规则（MEDIUM）

**前端：**
- 5张牌
- 必须恰好是3张相同 + 2张相同

**后端：**
- 没有三带二规则检查

### 6. 飞机规则（MEDIUM）

**前端：**
- 至少2个三张
- 必须连续
- 总张数必须是3的倍数

**后端：**
- 没有飞机规则检查

### 7. 级牌处理（CRITICAL）

**前端：**
- 级牌 val = levelRank 时，返回特殊值
- 红桃级牌 = 60（逢人配）
- 其他花色级牌 = 50

**后端：**
- 直接使用原始 val
- 没有级牌特殊值处理

## 修复方案

需要创建以下新函数来完全匹配前端行为：

1. **get_hand_type(p_cards, p_level_rank)** - 识别牌型
2. **is_valid_hand_type(p_cards, p_level_rank)** - 验证牌型有效性
3. **is_consecutive(p_values)** - 检查数组是否连续
4. **analyze_bomb(p_cards, p_level_rank)** - 分析炸弹类型和主值
5. **get_primary_value(p_cards, p_level_rank, p_hand_type)** - 获取牌型主值

## 修复后行为

完全匹配前端 rules.ts：
- 王炸（10000）> 级牌炸弹（5000×n）> 普通炸弹（1000×n）
- 级牌对子/三张使用正确的主值（50或60）
- 顺子不能包含2和王
- 连对必须连续且至少3对
- 飞机必须连续且至少2个三张
- 三带二必须恰好3+2

## 测试建议

